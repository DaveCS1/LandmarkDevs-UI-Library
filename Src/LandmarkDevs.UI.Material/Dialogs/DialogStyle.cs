namespace LandmarkDevs.UI.Material.Dialogs
{
    /// <summary>
    ///     An enum representing the different button states for a Message Dialog.
    /// </summary>
    public enum DialogStyle
    {
        /// <summary>
        ///     Just "OK"
        /// </summary>
        Ok = 0,

        /// <summary>
        ///     "OK" and "Cancel"
        /// </summary>
        OkNo = 1,

        /// <summary>
        ///     Ok, No, and Cancel
        /// </summary>
        OkNoCancel = 2,

        /// <summary>
        ///     Ok, No, Cancel, and Extra
        /// </summary>
        OkNoCancelExtra = 3
    }
}